class User {
  String id;
  String address;
  String city;
  String country;
  String email;
  String job;
  String sex;
  String number;
  String state;
  String name;

  User({
    required this.id,
    required this.address,
    required this.city,
    required this.country,
    required this.email,
    required this.job,
    required this.sex,
    required this.number,
    required this.state,
    required this.name,
  });
}
